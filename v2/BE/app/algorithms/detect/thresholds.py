# 档位(有序,索引即严重度)
GRADES = ["正常", "注意值1", "注意值2", "告警值"]
NORMAL, ATTENTION_1, ATTENTION_2, ALARM = GRADES

# ① 绝对浓度阈值:gas -> [注意值1 下限, 注意值2 下限, 告警值 下限]
# 达到某下限即进入该档;未达注意值1 即正常。None = 该档无定义(不再升)。
ABS_CONCENTRATION = {
    "c2h2": [1.0, 5.0, 8.0],
    "h2": [120.0, 150.0, None],
    "total_hydrocarbon": [120.0, 150.0, None],
}

# ② 绝对增量阈值(μL/L·周):gas -> [注意值1, 注意值2, 告警值]
# C₂H₂「从无到有」(参比≈0 且现值≥1)亦入注意值1,由算法层特判。
ABS_INCREMENT = {
    "c2h2": [1.0, 2.0, 4.0],
    "h2": [15.0, 30.0, None],
    "total_hydrocarbon": [15.0, 30.0, None],
}

# ③ 相对增长速率阈值(%/周):仅总烃;总烃<30μL/L 不计算此判据
REL_GROWTH = {
    "total_hydrocarbon": [30.0, 60.0, None],
}
REL_GROWTH_MIN_BASE = 30.0  # 总烃 <30μL/L 不算相对增长速率

# 产气速率处置研判门槛 = 注意值2(= DL/T 722 表3 经典注意值线)
URGENCY_TRIGGER_GRADE = ATTENTION_2

# ────────────────────────────────────────────────────────────────────
# DL/T 722 §9.3.2 相对产气速率(式2,%/月)—— 与表A.3(1498.2 %/周)是**两套
# 独立口径**(D-004):表A.3 那套只管①落档;722 这套管②处置研判 + ③趋势 + ④「预」。
# §9.3.2:总烃相对产气速率注意值 = 10%/月(总烃起始含量很低时不宜用此判据)。
# ────────────────────────────────────────────────────────────────────
THC_REL_RATE_ATTENTION = 10.0   # 总烃相对产气速率注意值,%/月(§9.3.2)
THC_REL_RATE_MIN_BASE = 10.0    # 总烃起始<10μL/L 不宜用相对速率(§9.3.2 括注)
# 逐日相对速率 = 真·月环比:今 vs 30天前,Δt=1月,γᵣ=(今−前30天)/前30天 ×100%(%/月)。
# 不做「短窗折月放大」(周环比×4.3 会把健康段随机日抖虚放成假超标)。
REL_RATE_LOOKBACK_DAYS = 30      # 月环比:跟 30 天前比,直接得 %/月
# 连续确认:相对速率须连续 N 天都超注意值才算「预/急」。健康段随机抖不会连超,
# 异常段真实爬升会持续超——滤掉健康段孤立误触发。
REL_RATE_CONSECUTIVE_DAYS = 3

# 参比值取值(DL/T 1498.2-2025 A.3.3 a):参比值 = 前14天~前7天窗口内测量数据的
# 算术平均值,计算前先剔除奇异值。绝对增量 γₐ = 当前值 − 参比值,相对增长速率
# γᵣ = (当前值 − 参比值)/参比值 ×100%。
